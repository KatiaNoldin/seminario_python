# Mostrar el uso de los operadores aritméticos en Python

# Ingreso de dos números
a = int(input("Ingrese el primer número: "))
b = int(input("Ingrese el segundo número: "))

# Suma
suma = a + b
print(f"Suma", suma)

# Resta
resta = a - b
print(f"Resta" , resta)

# Multiplicación
multiplicacion = a * b
print(f"Multiplicación" ,  multiplicacion)

# División
division = a / b
print(f"División real", division)


# Módulo (resto de la división)
modulo = a % b
print(f"Módulo", modulo)

# Potencia
potencia = a ** b
print(f"Potencia", potencia)